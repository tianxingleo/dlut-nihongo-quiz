// 为无法替换的汉字，提供一个 fallback 函数
export function fallbackAlt(headword, correctText, existingSet) {
  // 用 "異体字" 注释兜底：把某个汉字换成它的简化/繁体形式
  // 如果找不到，就替换一个汉字为通用形近字
  const chars = [...correctText]
  const kanjiIdxs = []
  for (let i = 0; i < chars.length; i++) {
    if (/[一-鿿]/.test(chars[i])) kanjiIdxs.push(i)
  }
  if (kanjiIdxs.length === 0) return null

  // 试每个汉字位置，把汉字换成"潦"或类似常用字
  const fallbackChars = ['字', '語', '詞', '文', '書', '記', '物', '事']
  for (const idx of kanjiIdxs) {
    for (const fc of fallbackChars) {
      const alt = [...chars]
      alt[idx] = fc
      const altStr = alt.join('')
      if (altStr !== correctText && !existingSet.has(altStr)) {
        return altStr
      }
    }
  }
  return null
}
